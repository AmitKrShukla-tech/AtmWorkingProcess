public class Atm {
    private double balance;
    private double depositeAamount;
    private double withdrawAmout;

    //default constructor

    public Atm() {
        this.balance = balance;
        this.depositeAamount = depositeAamount;
        this.withdrawAmout = withdrawAmout;
    }
    //getter setter
    public double getBalance() {
        return balance;
    }

    public void setBalance(double balance) {
        this.balance = balance;
    }

    public double getDepositeAamount() {
        return depositeAamount;
    }

    public void setDepositeAamount(double depositeAamount) {
        this.depositeAamount = depositeAamount;
    }

    public double getWithdrawAmout() {
        return withdrawAmout;
    }

    public void setWithdrawAmout(double withdrawAmout) {
        this.withdrawAmout = withdrawAmout;
    }
}
